package strings;

public class SubstringOfAString {
public static void main(String[] args) {
	
	
    String s1 = "Welcome to a morning class";
    
    System.out.println(s1.substring(8));
    System.out.println(s1.substring(8,15));
    
    String name = "Mr. Salil Sabnis";
    String formattedName = name.substring(4);
    System.out.println(formattedName);
}
}
